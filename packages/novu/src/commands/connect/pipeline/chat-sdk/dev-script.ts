import fs from 'node:fs';
import path from 'node:path';

import { readProjectPackageJson } from './project-package';
import { readProjectEnvValue } from './wire-env';

/** Default local port for Chat SDK connect — avoids 3000/4000 collisions. */
const DEFAULT_CHAT_SDK_DEV_PORT = 4005;

export type DevScriptApplyResult = {
  applied: boolean;
  script: string;
};

function readDevScript(projectDir: string): string {
  const pkg = readProjectPackageJson(projectDir);
  const scripts = pkg?.scripts;

  if (!scripts || typeof scripts !== 'object') {
    return '';
  }

  return String((scripts as Record<string, unknown>).dev ?? '').trim();
}

function stripWarnNoTunnelPrefix(devScript: string): string {
  return devScript.replace(/^node\s+warn-no-tunnel\.mjs\s+\S+\s+&&\s+/, '').trim();
}

function parsePortFromCommand(command: string): number | undefined {
  const portMatch = command.match(/--port(?:=|\s+)(\d+)/) ?? command.match(/(?:^|\s)-p(?:\s+|=)(\d+)/);

  if (!portMatch) {
    return undefined;
  }

  const port = Number.parseInt(portMatch[1], 10);

  return Number.isFinite(port) ? port : undefined;
}

function readPortFromEnvFiles(projectDir: string): number | undefined {
  const raw = readProjectEnvValue(projectDir, 'PORT');
  if (!raw) {
    return undefined;
  }

  const port = Number.parseInt(raw.trim(), 10);
  if (Number.isFinite(port) && port > 0) {
    return port;
  }

  return undefined;
}

export function inferDevPort(projectDir: string): number {
  const devScript = readDevScript(projectDir);
  const runCommand = stripWarnNoTunnelPrefix(devScript);

  const fromDevScript = parsePortFromCommand(runCommand) ?? parsePortFromCommand(devScript);
  if (fromDevScript) {
    return fromDevScript;
  }

  const fromEnv = readPortFromEnvFiles(projectDir);
  if (fromEnv) {
    return fromEnv;
  }

  if (/next\s+dev\b/.test(runCommand) || /next\s+dev\b/.test(devScript)) {
    return DEFAULT_CHAT_SDK_DEV_PORT;
  }

  return DEFAULT_CHAT_SDK_DEV_PORT;
}

export function inferDevRunCommand(projectDir: string): { port: number; runCommand: string } {
  const devScript = readDevScript(projectDir);
  const runCommand = stripWarnNoTunnelPrefix(devScript) || `next dev --port=${DEFAULT_CHAT_SDK_DEV_PORT}`;
  const port = inferDevPort(projectDir);

  return { port, runCommand: normalizeRunCommandForPort(runCommand, port) };
}

function normalizeRunCommandForPort(runCommand: string, port: number): string {
  if (parsePortFromCommand(runCommand)) {
    return runCommand;
  }

  if (/^next\s+dev\b/.test(runCommand)) {
    return `next dev --port=${port}`;
  }

  return runCommand;
}

export function hasDevNovuScript(projectDir: string): boolean {
  const pkg = readProjectPackageJson(projectDir);
  const scripts = pkg?.scripts;

  if (!scripts || typeof scripts !== 'object') {
    return false;
  }

  return typeof (scripts as Record<string, unknown>)['dev:novu'] === 'string';
}

function readDevNovuScript(projectDir: string): string | undefined {
  const pkg = readProjectPackageJson(projectDir);
  const scripts = pkg?.scripts;

  if (!scripts || typeof scripts !== 'object') {
    return undefined;
  }

  const script = (scripts as Record<string, unknown>)['dev:novu'];
  if (typeof script !== 'string') {
    return undefined;
  }

  const trimmed = script.trim();

  return trimmed.length > 0 ? trimmed : undefined;
}

/** True when an existing dev:novu script looks generated by us but uses a stale port. */
export function shouldRefreshDevNovuScript(projectDir: string): boolean {
  const existing = readDevNovuScript(projectDir);
  if (!existing) {
    return true;
  }

  if (!/npx\s+novu\s+dev\b/.test(existing)) {
    return false;
  }

  const expected = buildDevNovuScript(projectDir);

  return existing !== expected;
}

export function buildDevNovuScript(projectDir: string): string {
  const { port, runCommand } = inferDevRunCommand(projectDir);

  return `npx novu dev -p ${port} --no-studio --route /api/webhooks/novu --run "${runCommand}"`;
}

export function applyDevNovuScript(projectDir: string): DevScriptApplyResult {
  const packageJsonPath = path.join(projectDir, 'package.json');
  const pkg = readProjectPackageJson(projectDir);

  if (!pkg) {
    throw new Error(`Cannot add dev:novu script — missing package.json in ${projectDir}`);
  }

  const script = buildDevNovuScript(projectDir);
  const existing = readDevNovuScript(projectDir);

  if (existing && existing === script) {
    return { applied: false, script: existing };
  }

  if (existing && !shouldRefreshDevNovuScript(projectDir)) {
    return { applied: false, script: existing };
  }

  const scripts = pkg.scripts;
  const nextScripts =
    scripts && typeof scripts === 'object'
      ? { ...(scripts as Record<string, unknown>) }
      : ({} as Record<string, unknown>);

  nextScripts['dev:novu'] = script;
  pkg.scripts = nextScripts;
  fs.writeFileSync(packageJsonPath, `${JSON.stringify(pkg, null, 2)}\n`);

  return { applied: true, script };
}
