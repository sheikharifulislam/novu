import { ConflictException } from '@nestjs/common';
import { TopicRepository, TopicSubscribersRepository } from '@novu/dal';
import { expect } from 'chai';
import sinon from 'sinon';
import { SearchByExternalSubscriberIds } from '../../../subscribers/usecases';
import { CreateTopicUseCase } from '../create-topic';
import { AddSubscribersCommand } from './add-subscribers.command';
import { AddSubscribersUseCase } from './add-subscribers.use-case';

describe('AddSubscribersUseCase', () => {
  let useCase: AddSubscribersUseCase;
  let createTopicMock: sinon.SinonStubbedInstance<CreateTopicUseCase>;
  let searchSubscribersMock: sinon.SinonStubbedInstance<SearchByExternalSubscriberIds>;
  let topicSubscribersRepoMock: sinon.SinonStubbedInstance<TopicSubscribersRepository>;
  let topicRepoMock: sinon.SinonStubbedInstance<TopicRepository>;

  const mockTopicEntity = {
    _id: 'topic_id',
    _environmentId: 'env_id',
    _organizationId: 'org_id',
    key: 'test-topic',
    name: 'Autogenerated-test-topic',
  };

  const baseCommand = {
    environmentId: 'env_id',
    organizationId: 'org_id',
    userId: 'user_id',
    topicKey: 'test-topic',
    subscribers: ['sub-1'],
  };

  beforeEach(() => {
    createTopicMock = sinon.createStubInstance(CreateTopicUseCase);
    searchSubscribersMock = sinon.createStubInstance(SearchByExternalSubscriberIds);
    topicSubscribersRepoMock = sinon.createStubInstance(TopicSubscribersRepository);
    topicRepoMock = sinon.createStubInstance(TopicRepository);

    useCase = new AddSubscribersUseCase(
      createTopicMock as unknown as CreateTopicUseCase,
      searchSubscribersMock as unknown as SearchByExternalSubscriberIds,
      topicSubscribersRepoMock as unknown as TopicSubscribersRepository,
      topicRepoMock as unknown as TopicRepository
    );

    searchSubscribersMock.execute.resolves([]);
  });

  afterEach(() => {
    sinon.restore();
  });

  it('should recover from a duplicate key error during topic creation', async () => {
    topicRepoMock.findTopicByKey.onFirstCall().resolves(null);
    topicRepoMock.findTopicByKey.onSecondCall().resolves(mockTopicEntity as any);

    const mongoError = Object.assign(new Error('E11000 duplicate key'), { code: 11000 });
    createTopicMock.execute.rejects(mongoError);

    const command = AddSubscribersCommand.create(baseCommand);
    const result = await useCase.execute(command);

    expect(result).to.have.property('existingExternalSubscribers');
    expect(topicRepoMock.findTopicByKey.calledTwice).to.be.true;
    expect(createTopicMock.execute.calledOnce).to.be.true;
  });

  it('should recover from a ConflictException during topic creation', async () => {
    topicRepoMock.findTopicByKey.onFirstCall().resolves(null);
    topicRepoMock.findTopicByKey.onSecondCall().resolves(mockTopicEntity as any);

    createTopicMock.execute.rejects(new ConflictException('Topic exists'));

    const command = AddSubscribersCommand.create(baseCommand);
    const result = await useCase.execute(command);

    expect(result).to.have.property('existingExternalSubscribers');
    expect(topicRepoMock.findTopicByKey.calledTwice).to.be.true;
  });

  it('should re-throw non-duplicate errors during topic creation', async () => {
    topicRepoMock.findTopicByKey.resolves(null);
    createTopicMock.execute.rejects(new Error('Unexpected failure'));

    const command = AddSubscribersCommand.create(baseCommand);

    try {
      await useCase.execute(command);
      expect.fail('Should have thrown');
    } catch (err: any) {
      expect(err.message).to.equal('Unexpected failure');
    }
  });

  it('should skip topic creation when topic already exists', async () => {
    topicRepoMock.findTopicByKey.resolves(mockTopicEntity as any);

    const command = AddSubscribersCommand.create(baseCommand);
    const result = await useCase.execute(command);

    expect(result).to.have.property('existingExternalSubscribers');
    expect(createTopicMock.execute.called).to.be.false;
  });
});
